# Flatten the vocabulary into a single list
ALL_TERMS = []
for category, terms in VOCABULARY.items():
    ALL_TERMS.extend(terms)

# Remove duplicates and sort
ALL_TERMS = sorted(list(set(ALL_TERMS)))

# Additional utility functions for enhanced vocabulary matching
def get_category_terms(category):
    """Get all terms for a specific category"""
    return VOCABULARY.get(category, [])

def get_categories():
    """Get all available categories"""
    return list(VOCABULARY.keys())

def search_terms(query, case_sensitive=False):
    """Search for terms containing the query string"""
    query = query if case_sensitive else query.lower()
    matches = []
    for term in ALL_TERMS:
        search_term = term if case_sensitive else term.lower()
        if query in search_term:
            matches.append(term)
    return matches

def get_related_terms(term, max_results=10):
    """Get semantically related terms from the same category"""
    related = []
    for category, terms in VOCABULARY.items():
        if term in terms:
            related.extend([t for t in terms if t != term])
            break
    return related[:max_results]

def get_category_for_term(term):
    """Get the category for a specific term"""
    for category, terms in VOCABULARY.items():
        if term in terms:
            return category
    return None

